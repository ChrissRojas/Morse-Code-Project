class Morse_Code:
    morse = {
        " ": " / ",
        "a":".- ",
        "b":"-... ",
        "c":"-.-. ",
        "d":"-.. ",
        "e":". ",
        "f":"..-. ",
        "g":"--. ",
        "h":".... ",
        "i":".. ",
        "j":".--- ",
        "k":"-.- ",
        "l":".-.. ",
        "m":"-- ",
        "n":"-. ",
        "o":"--- ",
        "p":".--. ",
        "q":"--.- ",
        "r":".-. ",
        "s":"... ",
        "t":"- ",
        "u":"..- ",
        "v":"...- ",
        "w":".-- ",
        "x":"-..- ",
        "y":"-.-- ",
        "z":"--.. ",
        "1":".---- ",
        "2":"..--- ",
        "3":"...-- ",
        "4":"....- ",
        "5":"..... ",
        "6":"-.... ",
        "7":"--... ",
        "8":"---.. ",
        "9":"----. ",
        "0":"----- "
    }

    def convert(self, word):
        morse_code = ""
        for i in word:
            morse_code += self.morse[i]
        return morse_code

    def print_morse(self,word):
        print(self.convert(word))

